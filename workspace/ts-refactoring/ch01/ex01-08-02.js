"use strict";
//  다음과 같은 a,b가 주어졌을 때, +,-,*,/,%,** 산술 연산자를 사용하여 계산된 결과를 출력하세요.
//  출력결과:
//  13
//  7
//  30
//  3.3333333333333335
//  1
//  1000
(() => {
    let a = 10;
    let b = 3;
    console.log(a + b);
    console.log(a - b);
    console.log(a * b);
    console.log(a / b);
    console.log(a % b);
    console.log(a ** b); // a ^ 3;
})();
